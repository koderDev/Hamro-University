"""Demonstrate primitive types, arithmetic, and f-strings."""

name = "Aditi"
age = 20
pi = 3.14159
is_student = True

print(f"Name: {name}")
print(f"Age next year: {age + 1}")
print(f"First three letters of the name: {name[:3]}")
print(f"Pi rounded to 2 decimals: {pi:.2f}")
print(f"Is student? {is_student}")
